package com.example;

public class Calculadora {

    public Integer soma(Integer v1, Integer v2) {

        return v1 + v2;
    }

    public Integer somaSub(Integer v1, Integer v2) {

        return v1 - v2;
    }

    public Integer somaDiv(Integer v1, Integer v2) {

        return v1 / v2;
    }

    public Integer somaMult(Integer v1, Integer v2) {

        return v1 * v2;
    }

}
